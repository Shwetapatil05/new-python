#-------------------------------------------------------
#Description : while loop
#syntax      : 
#     while(condition):
#       statements;
#About       : Iterating through a given condition
#-------------------------------------------------------

print("-------------Iterating through given condition----------------");
i = 7
while i!=12:
  print(i);
  i += 1; 

